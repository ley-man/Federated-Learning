def test(net, testloader):
    """Validate the model on the test set."""
    criterion = torch.nn.CrossEntropyLoss()
    correct, loss = 0, 0.0
    with torch.no_grad():
        for images, labels in tqdm(testloader):
            outputs = net(images.to(DEVICE))
            labels = labels.to(DEVICE)
            loss += criterion(outputs, labels).item()
            correct += (torch.max(outputs.data, 1)[1] == labels).sum().item()
    accuracy = correct / len(testloader.dataset)
    return loss, accuracy


# Data is partitioned on the basis on num_client(total clients) and client_num (nth client)
def load_data():
    """Load CIFAR-10 (training and test set) from data_split folder"""
    B = 50
    train_path = "../data_split/training_data_client4.pt"
    test_path = "../data_split/testing_data_client4.pt"
    trainset = torch.load(train_path)
    testset = torch.load(test_path)
    trainloader = DataLoader(trainset, batch_size=B, shuffle=True)
    testloader = DataLoader(testset, batch_size=B, shuffle=True)
    num_examples = {"trainset" : len(trainset), "testset" : len(testset)}
    return trainloader, testloader


